# math operators in python
# addition is done with the +
addition_results = 10 + 10
addition_results_f = 10 + 10.0

print(addition_results, addition_results_f)

# subtraction is done with the -
sub = 10 - 5
print(sub)

# multiplication is done with the *
multiples = 10 * 3
print(multiples)

# Division
# in Python 3
div = 10/3
print(div)

# Integer Division
int_div = 10//3
print(int_div)

# Get the remainder with the modulo %
rem = 10 % 3
print(rem)

# Get exponetnail info
exp = 3 ** 2
print(exp)

# Floating point numbers are formed wiht a .
num = 0.0
for i in range(0, 1000):
    num += .01
    print(num)